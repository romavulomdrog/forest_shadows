using System.Collections.Generic;
using UnityEngine;


public class NumberCar : MonoBehaviour
{
    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


    public List<NumberCarSelection> numberCar = new List<NumberCarSelection>();

    //--------------

    Transform tr;


    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


    void Awake()
    {
        //--------------

        tr = transform;

        //--------------
    }


    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


    void OnEnable()
    {
        //--------------

        float localSeed = GlVAR.worldSeed + tr.position.z + tr.position.x + tr.rotation.x + tr.rotation.y + tr.rotation.z;

        for (int i = 0; i < numberCar.Count; i++)
        {
            Random.InitState(Mathf.RoundToInt(Mathf.Cos(localSeed + numberCar[i].amount.x + numberCar[i].amount.y) * 10000));
            float var0 = Random.Range(numberCar[i].amount.x, numberCar[i].amount.y);
            numberCar[i].numberText.text = var0.ToString();
        }

        //--------------
    }


    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
}


///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


[System.Serializable]

public class NumberCarSelection
{
    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


    public TextMesh numberText;
    public Vector2Int amount;


    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
}