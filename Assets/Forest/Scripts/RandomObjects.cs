using System.Collections.Generic;
using UnityEngine;


public class RandomObjects : MonoBehaviour
{
    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


    public List<ObjectSelection> ObjectSelectionList = new List<ObjectSelection>();

    //--------------

    Transform tr;
    int numObjectsList;


    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


    void OnEnable()
    {
        //--------------        

        tr = transform;
        numObjectsList = ObjectSelectionList.Count;

        Random.InitState(Mathf.RoundToInt(Mathf.Cos(GlVAR.worldSeed + tr.position.x + tr.position.z) * 10000));
        float var0 = Random.value;

        for (int i = 0; i < numObjectsList; i++)
        {
            if (ObjectSelectionList[i].prefab.activeSelf == true) ObjectSelectionList[i].prefab.SetActive(false);
            if (var0 > ObjectSelectionList[i].positionGeneration.x && var0 < ObjectSelectionList[i].positionGeneration.y) ObjectSelectionList[i].prefab.SetActive(true);
        }

        //--------------
    }


    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
}


///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


[System.Serializable]

public class ObjectSelection
{
    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


    public GameObject prefab;
    public Vector2 positionGeneration;


    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
}