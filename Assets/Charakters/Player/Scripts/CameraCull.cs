using System.Collections.Generic;
using UnityEngine;


///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


public class CameraCull : MonoBehaviour
{
    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


    public bool layerCullSpherical;
    public List<LayerCull> layerList = new List<LayerCull>();


    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


    void Awake()
    {
        //--------------

        Camera camera = GetComponent<Camera>();
        float[] distances = new float[32];
        for (int lc = 0; lc < layerList.Count; lc++) distances[layerList[lc].layer] = layerList[lc].distances;
        camera.layerCullDistances = distances;
        camera.layerCullSpherical = layerCullSpherical;

        //--------------
    }


    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
}


///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


[System.Serializable]

public class LayerCull
{
    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


    public int layer;
    public float distances = 100.0f;


    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
}